"""
Per-squad Assault Fire / Spraying Fire capability, read from VASL counter art.

The same FP-Range-Morale string does NOT determine capability by itself (it
varies by nationality and squad class: the Russian SMG 6-2-8 has Assault
Fire, the NKVD 6-2-8 does not), but the rulebook defines the capability as a
property printed on the counter:

  A1.21 FIREPOWER (verified verbatim, eASLRB v3.14): "If the FP number is
    underlined, the unit may use the Assault FP bonus (7.36) during its AFPh."
  A7.36 ASSAULT FIRE (verified verbatim): "Assault Fire is restricted to
    squads armed primarily with SMG/semi-automatic rifles, as well as
    appropriate training in their use. Such squads are identified by the
    underscoring of their FP factor. Assault Fire capability allows any squad
    using its inherent FP during the AFPh to add one FP to its Small Arms
    Fire attack after all modification to the squad's inherent FP; any
    fraction in its FP is then rounded up. The Assault Fire bonus is not
    applicable to Opportunity Fire or Long Range Fire, but is still
    applicable to pinned-firers/Spraying-Fire in the AFPh."
  A7.34 SQUAD SPRAYING FIRE (verified verbatim): "Any squad with a Spraying
    Fire option is identified by the underscore beneath the Range portion of
    its Strength Factor. Squad Spraying Fire is identical to MG Spraying Fire
    (9.5) except that it is limited to a maximum range of three hexes."

VASL 6.7.3 draws those underscores as explicit, machine-readable SVG
elements: every squad counter SVG (images/<nat>/<nat><FP><Range><Morale>
<class><variant>.svg) contains
    <line id="AssaultFire" .../>   for the underscored FP factor, and/or
    <line id="SprayingFire" .../>  for the underscored Range factor.
ART_CAPABILITIES below was generated by scanning every squad/HS/crew SVG of
the 8 covered nationalities in ~/vasl/vasl-6.7.3.vmod for those element ids
(165 counters; art lacking a printed Strength Factor — broken sides, blank
placeholders — was skipped). Spot checks against the eASLRB text all agree:

  * A7.31 EX gives the American 6-6-6 and German 5-4-8 the Assault Fire +1
    (art: am666S/ge548S have AssaultFire).
  * The A25.11 SS designer note: the morale factor "of Late War SS MMC
    [EXC: SS 8-3-8s/3-3-8s] is never considered underscored" and "SS 6-5-8
    squads are entitled to Assault Fire capabilities in any scenario during
    1944-45" — i.e. the 6-5-8 counter itself is NOT underscored (art agrees:
    ge658Ss has no AssaultFire element); the 1944-45 grant is a conditional
    NOT printed on the counter, surfaced via CONDITIONAL_NOTES.
  * The A25 Russian NKVD designer note: "its combat deficiencies are
    reflected by its 2nd-Line status and the lack of Assault Fire capability
    normally associated with SMG-armed squads" (art agrees: ru628SN has
    SprayingFire only, vs the regular SMG ru628S which has both).
  * A25.53: "in/after 12/43, Free French squads have Assault Fire (7.36)
    capability" — conditional, not printed on French counters (art agrees:
    no French squad has an underscore); surfaced via CONDITIONAL_NOTES.

One art oddity found (reported, kept as scanned): the SS conscript 4-3-6
(ge436Ss) lacks the SprayingFire element while the regular German 4-3-6
(ge436S) has it. Encoded as scanned — the counter art is the rule's own
definition of the capability (A1.21/A7.34) — but flagged in the entry note.

Nationalities NOT covered (Axis Minor, Allied Minor, Chinese, Partisan, ...)
fall through to (None, None) = "unknown": callers must warn instead of
applying a capability. Note A25.9: "Polish and Belgian elite and 1st Line
squads should generally be assigned Assault Fire (7.36) via a SSR."
"""
