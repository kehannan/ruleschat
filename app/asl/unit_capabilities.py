# Conditional capabilities granted by rule/SSR, NOT printed on the counter —
# surfaced as notes, never auto-applied.
CONDITIONAL_NOTES: Dict[str, str] = {
    "ge658Ss": ("SS 6-5-8: entitled to Assault Fire in any 1944-45 scenario "
                "(A25.11 note) — not printed on the counter, NOT auto-applied"),
}
_FREE_FRENCH_NOTE = ("French squad: Free French squads have Assault Fire "
                     "in/after 12/43 (A25.53) — not printed on the counter, "
                     "NOT auto-applied")

# Side name (as parse_vsav emits it) -> VASL art prefix, for the name-based
# fallback key. Only the 8 scanned nationalities.
_SIDE_PREFIX = {"German": "ge", "Russian": "ru", "American": "am",
                "British": "br", "Finnish": "fi", "Italian": "it",
                "Japanese": "ja", "French": "fr"}

_STEM_RE = re.compile(r"^(ge|ru|am|br|fi|it|ja|fr)(\d)(\d)(\d)(S|H|C)")
# "6-4-8 1sq" / "4-5-8 Esq" / "2-2-8 Icr" / "3-4-8 2hs" -> strength + class.
_NAME_RE = re.compile(r"^(\d+(?:\.5)?-\d+-\d+)\b.*?(sq|hs|cr)\s*$",
                      re.IGNORECASE)
_CLASS_LETTER = {"sq": "S", "hs": "H", "cr": "C"}


def _build_name_fallback() -> Dict[Tuple[str, str, str], Tuple[Optional[bool], Optional[bool]]]:
    """(art prefix, 'FP-Range-Morale', class letter) -> capabilities.

    Aggregated over all art variants of the same printed strength/class
    (Sae, Ss, SN, _para, ...): a capability is only asserted when EVERY
    variant agrees; otherwise None ("ambiguous — depends on the exact
    counter", e.g. Russian 6-2-8 SMG vs NKVD for Assault Fire). "_sap"
    alternate-art variants are excluded from aggregation (one of them,
    ge447S_sap, drops an underscore its base art has).
    """
    agg: Dict[Tuple[str, str, str], list] = {}
    for stem, caps in ART_CAPABILITIES.items():
        if "_sap" in stem:
            continue
        m = _STEM_RE.match(stem)
        if not m:
            continue
        key = (m.group(1), f"{m.group(2)}-{m.group(3)}-{m.group(4)}",
               m.group(5))
        agg.setdefault(key, []).append(caps)
    out = {}
    for key, values in agg.items():
        af_vals = {v[0] for v in values}
        sf_vals = {v[1] for v in values}
        out[key] = (af_vals.pop() if len(af_vals) == 1 else None,
                    sf_vals.pop() if len(sf_vals) == 1 else None)
    return out


NAME_FALLBACK = _build_name_fallback()


def art_stem(art: Optional[str]) -> Optional[str]:
    """'fi/fi648S.svg' (or 'fi648S.gif', or a full VASL path) -> 'fi648S'."""
    if not art:
        return None
    base = art.replace("\\", "/").rsplit("/", 1)[-1]
    return base.rsplit(".", 1)[0] or None


def lookup(art: Optional[str] = None, side: Optional[str] = None,
           name: Optional[str] = None) -> Dict[str, Any]:
    """Capability lookup for one personnel counter.

    Returns {"assault_fire": True/False/None, "spraying_fire": ditto,
    "source": str|None, "note": str|None}. None = unknown — the caller must
    warn rather than apply. Primary key: exact counter-art stem; fallback:
    (nationality, FP-Range-Morale string, squad/HS/crew class from the VASL
    piece name), which is only asserted where unambiguous across counters.
    """
    out: Dict[str, Any] = {"assault_fire": None, "spraying_fire": None,
                           "source": None, "note": None}
    stem = art_stem(art)
    if stem and stem in ART_CAPABILITIES:
        af, sf = ART_CAPABILITIES[stem]
        out.update(assault_fire=af, spraying_fire=sf,
                   source=f"counter art {stem} (VASL 6.7.3 underscore elements)")
        out["note"] = CONDITIONAL_NOTES.get(stem) or (
            _FREE_FRENCH_NOTE if stem.startswith("fr") and stem[5:6] == "S"
            else None)
        return out
    prefix = _SIDE_PREFIX.get(side or "")
    m = _NAME_RE.match((name or "").strip())
    if prefix and m:
        key = (prefix, m.group(1), _CLASS_LETTER[m.group(2).lower()])
        if key in NAME_FALLBACK:
            af, sf = NAME_FALLBACK[key]
            out.update(
                assault_fire=af, spraying_fire=sf,
                source=f"nationality + printed strength fallback "
                       f"({side} {m.group(1)} {m.group(2).lower()})")
            if af is None or sf is None:
                out["note"] = ("capability ambiguous for this strength/class "
                               "across counter variants — exact counter art "
                               "needed")
            if prefix == "fr" and key[2] == "S":
                out["note"] = _FREE_FRENCH_NOTE
            return out
    return out
